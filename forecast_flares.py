def train_model(X_train, y_train, X_val, y_val, feature_names):
    """Train an XGBoost classifier for flare prediction."""

    # Calculate class weight
    n_pos = y_train.sum()
    n_neg = len(y_train) - n_pos
    scale_pos_weight = n_neg / max(n_pos, 1) * 0.5  # Moderate upweighting

    print(f"\n  Training samples: {len(y_train)} (pos={n_pos}, neg={n_neg})")
    print(f"  Validation samples: {len(y_val)} (pos={y_val.sum()}, neg={len(y_val)-y_val.sum()})")
    print(f"  Scale pos weight: {scale_pos_weight:.1f}")

    model = xgb.XGBClassifier(
        n_estimators=300,
        max_depth=6,
        learning_rate=0.05,
        scale_pos_weight=scale_pos_weight,
        min_child_weight=5,
        subsample=0.8,
        colsample_bytree=0.8,
        reg_alpha=0.1,
        reg_lambda=1.0,
        eval_metric="aucpr",
        random_state=42,
        verbosity=0,
    )

    model.fit(
        X_train, y_train,
        eval_set=[(X_val, y_val)],
        verbose=False,
    )

    return model


