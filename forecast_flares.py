def evaluate_model(model, X_test, y_test, times_test, catalog, feature_names, horizon_name):
    """Comprehensive evaluation of the forecasting model."""

    y_prob = model.predict_proba(X_test)[:, 1]
    y_pred = (y_prob >= 0.5).astype(int)

    print(f"\n{'='*60}")
    print(f"EVALUATION RESULTS ({horizon_name} horizon)")
    print(f"{'='*60}")

    # Basic metrics
    print("\nClassification Report:")
    print(classification_report(y_test, y_pred, target_names=["No Flare", "Flare"]))

    # AUC scores
    if y_test.sum() > 0 and (1 - y_test).sum() > 0:
        roc_auc = roc_auc_score(y_test, y_prob)
        pr_auc = average_precision_score(y_test, y_prob)
        print(f"ROC-AUC: {roc_auc:.4f}")
        print(f"PR-AUC:  {pr_auc:.4f}")
    else:
        roc_auc = 0
        pr_auc = 0
        print("Cannot compute AUC (only one class in test set)")

    # Confusion matrix
    cm = confusion_matrix(y_test, y_pred)
    tn, fp, fn, tp = cm.ravel()
    print(f"\nConfusion Matrix:")
    print(f"  True Positives:  {tp}")
    print(f"  True Negatives:  {tn}")
    print(f"  False Positives: {fp} (false alarms)")
    print(f"  False Negatives: {fn} (missed flares)")

    tpr = tp / max(tp + fn, 1)
    far = fp / max(fp + tn, 1)
    print(f"\n  True Positive Rate:  {tpr:.4f}")
    print(f"  False Alarm Rate:    {far:.4f}")

    # Lead time analysis
    lead_times = compute_lead_times(y_prob, times_test, catalog, threshold=0.5)

    # Feature importance
    plot_feature_importance(model, feature_names, horizon_name)

    # ROC and PR curves
    plot_curves(y_test, y_prob, horizon_name, roc_auc, pr_auc)

    return {
        "roc_auc": roc_auc,
        "pr_auc": pr_auc,
        "tpr": tpr,
        "far": far,
        "tp": tp, "tn": tn, "fp": fp, "fn": fn,
        "lead_times": lead_times,
    }


def compute_lead_times(y_prob, times_test, catalog, threshold=0.5):
    """
    For each flare in the catalog, compute how many minutes BEFORE
    the flare start the model first triggered an alert.

    This is the KEY metric judges care about.
    """
    print(f"\n--- Lead Time Analysis ---")

    lead_times = []

    for flare in catalog:
        flare_start = flare["start_time"]
        flare_id = flare.get("flare_id", "?")
        flare_class = flare.get("flare_class", "?")

        # Find predictions in the window before this flare
        pre_flare_mask = (times_test >= flare_start - 1800) & (times_test < flare_start)
        if pre_flare_mask.sum() == 0:
            continue

        pre_flare_probs = y_prob[pre_flare_mask]
        pre_flare_times = times_test[pre_flare_mask]

        # Find first alert (first time prob exceeds threshold)
        alert_mask = pre_flare_probs >= threshold
        if alert_mask.sum() > 0:
            first_alert_idx = np.where(alert_mask)[0][0]
            first_alert_time = pre_flare_times[first_alert_idx]
            lead_time_sec = flare_start - first_alert_time
            lead_time_min = lead_time_sec / 60.0
            lead_times.append(lead_time_min)
            print(f"  {flare_id} ({flare_class}-class): Alert {lead_time_min:.1f} min before flare")
        else:
            print(f"  {flare_id} ({flare_class}-class): MISSED (no alert triggered)")
            lead_times.append(0)

    if lead_times:
        valid = [lt for lt in lead_times if lt > 0]
        if valid:
            print(f"\n  Average lead time: {np.mean(valid):.1f} minutes")
            print(f"  Median lead time:  {np.median(valid):.1f} minutes")
            print(f"  Min lead time:     {np.min(valid):.1f} minutes")
            print(f"  Max lead time:     {np.max(valid):.1f} minutes")
            print(f"  Flares with alert: {len(valid)}/{len(lead_times)}")

    return lead_times


