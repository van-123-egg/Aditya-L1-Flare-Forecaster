def plot_feature_importance(model, feature_names, horizon_name):
    """Plot top 20 most important features."""
    importance = model.feature_importances_
    indices = np.argsort(importance)[-20:]

    fig, ax = plt.subplots(figsize=(10, 8))
    ax.barh(range(len(indices)), importance[indices], color="#1976D2")
    ax.set_yticks(range(len(indices)))
    ax.set_yticklabels([feature_names[i] for i in indices], fontsize=9)
    ax.set_xlabel("Feature Importance (Gain)", fontsize=11)
    ax.set_title(f"Top 20 Features - {horizon_name} Forecast", fontsize=13, fontweight="bold")
    ax.grid(True, alpha=0.3, axis="x")
    plt.tight_layout()
    plt.savefig(f"feature_importance_{horizon_name}.png", dpi=150, bbox_inches="tight")
    plt.close()
    print(f"  Saved: feature_importance_{horizon_name}.png")


def plot_curves(y_test, y_prob, horizon_name, roc_auc, pr_auc):
    """Plot ROC and Precision-Recall curves."""
    fig, (ax1, ax2) = plt.subplots(1, 2, figsize=(12, 5))

    # ROC curve
    if roc_auc > 0:
        fpr, tpr, _ = roc_curve(y_test, y_prob)
        ax1.plot(fpr, tpr, color="#1976D2", linewidth=2, label=f"AUC = {roc_auc:.3f}")
        ax1.plot([0, 1], [0, 1], "k--", alpha=0.3, label="Random")
        ax1.set_xlabel("False Positive Rate")
        ax1.set_ylabel("True Positive Rate")
        ax1.set_title(f"ROC Curve - {horizon_name}", fontweight="bold")
        ax1.legend()
        ax1.grid(True, alpha=0.3)

    # Precision-Recall curve
    if pr_auc > 0:
        prec, rec, _ = precision_recall_curve(y_test, y_prob)
        ax2.plot(rec, prec, color="#E65100", linewidth=2, label=f"AP = {pr_auc:.3f}")
        baseline = y_test.sum() / len(y_test)
        ax2.axhline(y=baseline, color="k", linestyle="--", alpha=0.3, label=f"Baseline = {baseline:.3f}")
        ax2.set_xlabel("Recall")
        ax2.set_ylabel("Precision")
        ax2.set_title(f"Precision-Recall Curve - {horizon_name}", fontweight="bold")
        ax2.legend()
        ax2.grid(True, alpha=0.3)

    plt.tight_layout()
    plt.savefig(f"eval_curves_{horizon_name}.png", dpi=150, bbox_inches="tight")
    plt.close()
    print(f"  Saved: eval_curves_{horizon_name}.png")


def plot_predictions_timeline(times_test, y_test, y_prob, catalog, horizon_name):
    """Plot prediction probabilities over time with actual flares marked."""
    # Convert to hours from start
    t0 = times_test[0]
    hours = (times_test - t0) / 3600.0

    fig, (ax1, ax2) = plt.subplots(2, 1, figsize=(16, 8), sharex=True)

    # Panel 1: Prediction probability
    ax1.fill_between(hours, 0, y_prob, alpha=0.4, color="#1976D2", label="Flare probability")
    ax1.axhline(y=0.5, color="red", linestyle="--", alpha=0.5, label="Alert threshold (0.5)")
    ax1.set_ylabel("Probability", fontsize=11)
    ax1.set_title(f"Flare Forecast Probabilities - {horizon_name} horizon", fontsize=13, fontweight="bold")
    ax1.legend(loc="upper right", fontsize=9)
    ax1.set_ylim(0, 1.05)
    ax1.grid(True, alpha=0.3)

    # Panel 2: Actual labels
    ax2.fill_between(hours, 0, y_test, alpha=0.4, color="#4CAF50", step="mid", label="Actual (flare imminent)")
    ax2.set_ylabel("Actual Label", fontsize=11)
    ax2.set_xlabel("Hours from start of test set", fontsize=11)
    ax2.set_title("Ground Truth", fontsize=13, fontweight="bold")
    ax2.legend(loc="upper right", fontsize=9)
    ax2.set_ylim(-0.05, 1.5)
    ax2.grid(True, alpha=0.3)

    # Mark flare peaks
    for flare in catalog:
        peak_t = flare["peak_time"]
        if peak_t >= times_test[0] and peak_t <= times_test[-1]:
            peak_h = (peak_t - t0) / 3600.0
            fclass = flare.get("flare_class", "?")
            for ax in [ax1, ax2]:
                ax.axvline(x=peak_h, color="red", alpha=0.4, linewidth=1)
            ax1.annotate(f'{fclass}', xy=(peak_h, 0.95), fontsize=8, fontweight="bold",
                        color="red", ha="center")

    plt.tight_layout()
    plt.savefig(f"forecast_timeline_{horizon_name}.png", dpi=150, bbox_inches="tight")
    plt.close()
    print(f"  Saved: forecast_timeline_{horizon_name}.png")


