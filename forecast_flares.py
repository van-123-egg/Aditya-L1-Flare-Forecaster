def main():
    print("[*] Solar Flare Forecasting Model")
    print("=" * 60)

    # 1. Load data
    print("\n[1] Loading all lightcurve data...")
    times, counts = load_all_data()

    # 2. Load flare catalog
    print("\n[2] Loading flare catalog...")
    catalog = load_flare_catalog()
    print(f"  {len(catalog)} flares in catalog")

    # 3. Engineer features
    print("\n[3] Engineering features (30-min sliding window)...")
    X, feature_names, sample_indices = engineer_features(times, counts)
    sample_times = times[sample_indices]

    # 4. Create labels for each horizon
    print("\n[4] Creating labels...")
    for name, horizon_sec in HORIZONS.items():
        labels = create_labels(times, catalog, horizon_sec)
        y = labels[sample_indices]
        print(f"  {name}: {y.sum()} positive samples ({y.sum()/len(y)*100:.2f}%)")

    # Use primary horizon
    primary_labels = create_labels(times, catalog, HORIZONS[PRIMARY_HORIZON])
    y = primary_labels[sample_indices]

    # 5. Temporal train/val/test split
    # IMPORTANT: No random split! Use time-based split to prevent data leakage.
    print("\n[5] Splitting data (temporal split)...")
    n = len(X)
    train_end = int(n * 0.6)
    val_end = int(n * 0.8)

    X_train, y_train = X[:train_end], y[:train_end]
    X_val, y_val = X[train_end:val_end], y[train_end:val_end]
    X_test, y_test = X[val_end:], y[val_end:]
    times_test = sample_times[val_end:]

    print(f"  Train: {len(X_train)} samples (days 1-4)")
    print(f"  Val:   {len(X_val)} samples (days 5-6)")
    print(f"  Test:  {len(X_test)} samples (days 6-7)")

    # 6. Scale features
    print("\n[6] Scaling features...")
    scaler = StandardScaler()
    X_train_s = scaler.fit_transform(X_train)
    X_val_s = scaler.transform(X_val)
    X_test_s = scaler.transform(X_test)

    # 7. Train model
    print("\n[7] Training XGBoost model...")
    model = train_model(X_train_s, y_train, X_val_s, y_val, feature_names)

    # 8. Evaluate
    print("\n[8] Evaluating on test set...")

    # Find flares that fall in the test period
    test_start = times_test[0]
    test_end = times_test[-1]
    test_catalog = [f for f in catalog
                    if f["start_time"] >= test_start and f["start_time"] <= test_end]
    print(f"  Flares in test period: {len(test_catalog)}")

    results = evaluate_model(model, X_test_s, y_test, times_test, test_catalog,
                            feature_names, PRIMARY_HORIZON)

