def create_labels(times, catalog, horizon_sec):
    """
    Create binary labels: 1 if a flare starts within the next 'horizon_sec' seconds.

    For each time step t:
      label[t] = 1  if any flare START is within [t, t + horizon_sec]
      label[t] = 0  otherwise

    IMPORTANT: We label based on flare START time (not peak).
    This means we're predicting the ONSET, giving maximum lead time.
    """
    labels = np.zeros(len(times), dtype=np.int32)

    flare_starts = [f["start_time"] for f in catalog]
    flare_classes = [f.get("flare_class", "B") for f in catalog]

    for start_t, fclass in zip(flare_starts, flare_classes):
        # Find time steps where this flare is within the horizon
        mask = (times >= start_t - horizon_sec) & (times < start_t)
        labels[mask] = 1

    return labels


def create_multiclass_labels(times, catalog, horizon_sec):
    """
    Multi-class labels:
      0 = no flare expected
      1 = B/C class flare expected
      2 = M/X class flare expected
    """
    labels = np.zeros(len(times), dtype=np.int32)

    for flare in catalog:
        start_t = flare["start_time"]
        fclass = flare.get("flare_class", "B")

        mask = (times >= start_t - horizon_sec) & (times < start_t)

        if fclass in ("M", "X"):
            labels[mask] = 2
        else:
            labels[mask] = np.maximum(labels[mask], 1)

    return labels


